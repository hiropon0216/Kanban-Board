/**
 * ============================================================
 * validation.ts — 入力バリデーション関数
 * ============================================================
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 【バリデーションの原則】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * バリデーション（入力検証）は「ユーザーが入力した値が期待通りか確認する」処理です。
 *
 * フロントエンドでバリデーションする理由:
 *   1. UX（ユーザー体験）向上 → サーバーに送らずとも即座にフィードバック
 *   2. 不正データの事前除去  → サーバーの負荷軽減
 *   3. 必須項目の漏れ防止   → 保存後のデータ不整合を防ぐ
 *
 * ⚠️ 注意: フロントエンドのバリデーションは「利便性」のため。
 *   セキュリティのためには必ずバックエンド側でも検証すること。
 *   DevTools で JavaScript を書き換えれば、フロントのチェックは簡単に回避できる。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 【戻り値の設計: string | null】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * この関数たちは「エラーメッセージ or null」を返します。
 *
 *   null   → バリデーション通過（問題なし）
 *   string → エラーメッセージ（何が問題かをユーザーに伝える文字列）
 *
 * 呼び出し側はこう書けます:
 *
 *   const error = validateCardTitle(title);
 *   if (error) {
 *     setError(error);  // エラーメッセージを表示
 *     return;           // 送信を中断
 *   }
 *   // ここに来たら必ず error は null（= 問題なし）
 *   submitForm();
 *
 * boolean（true/false）を返す設計も考えられますが、
 * 「何がダメか」を伝えられないため UX が劣ります。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 【Pure Function（純粋関数）】
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * バリデーション関数は「純粋関数」として設計されています。
 *
 *   純粋関数の条件:
 *   1. 同じ入力には必ず同じ出力を返す（決定論的）
 *   2. 副作用がない（外部状態を変更しない、HTTP 通信しない、など）
 *
 * 純粋関数のメリット:
 *   - テストが簡単（入力と出力だけ確認すればよい）
 *   - 再利用しやすい（どこからでも呼べる）
 *   - バグを混入しにくい（状態に依存しないため）
 */

/**
 * カードタイトルのバリデーション。
 *
 * @param title - ユーザーが入力したタイトル文字列
 * @returns エラーメッセージ（問題あり）または null（問題なし）
 *
 * ルール:
 *   - 空白のみの文字列は「未入力」とみなす（.trim() で前後の空白を除去して判定）
 *   - 100 文字を超えてはいけない（UI 表示が崩れるため）
 *
 * `trim()` とは: 文字列の先頭と末尾の空白文字（スペース、タブ、改行）を除去する。
 *   "  Hello  ".trim() → "Hello"
 *   "   ".trim()       → ""  ← 空文字になるので !trimmed が true になる
 */
export function validateCardTitle(title: string): string | null {
  const trimmed = title.trim();
  if (!trimmed) return 'タイトルを入力してください';
  if (trimmed.length > 100) return 'タイトルは 100 文字以内で入力してください';
  return null;
}

/**
 * カード説明文のバリデーション。
 *
 * @param description - ユーザーが入力した説明文字列
 * @returns エラーメッセージ（問題あり）または null（問題なし）
 *
 * 説明は「任意」（入力しなくてよい）なので、空文字はエラーにしない。
 * 長さの上限だけチェックする。
 *
 * ここでは `.trim()` を使わずに長さを計算している。
 * 理由: 説明文は途中に改行を含むことがあり、trim すると正確な長さが変わる。
 *       また「空白だけの説明」も入力として受け入れてよいため。
 */
export function validateCardDescription(description: string): string | null {
  if (description.length > 500) return '説明は 500 文字以内で入力してください';
  return null;
}
